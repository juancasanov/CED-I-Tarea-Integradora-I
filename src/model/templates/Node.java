package model.templates;

public abstract class Node<T extends Comparable<T>>{
    private T t;

    private Node<T> next;

    public Node(T t){
        this.t = t;
    }

    public T getT() {
        return t;
    }

    public void setT(T t) {
        this.t = t;
    }

    public Node<T> getNext() {
        return next;
    }

    public void setNext(Node<T> next) {
        this.next = next;
    }
}